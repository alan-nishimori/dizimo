public class Dizimista {
    int codigo;
    String nome, endereco, cidade, uf, conjuge;

    public Dizimista(int cod, String name, String address, String city, String state, String partner){
        this.codigo = cod;
        this.nome = name;
        this.endereco = address;
        this.cidade = city;
        this.uf = state;
        this.conjuge = partner;
    }

}
